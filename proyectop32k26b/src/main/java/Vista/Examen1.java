    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(txtCodigo.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Ingrese un código");
        return;
    }

    clsFacultades facultades = new clsFacultades();
    facultades.setFaculcodigo(Integer.parseInt(txtCodigo.getText()));

    FacultadesDAO dao = new FacultadesDAO();
    clsBitacora bitacora = crearBitacora("Eliminar facultades");
dao.eliminarFacultades(facultades, bitacora);

    JOptionPane.showMessageDialog(null, "Facultades eliminado");

    llenadoDeTablas();
    limpiarTextos();
    
    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(txtNombre.getText().isEmpty() || txtEstado.getText().isEmpty()){
    JOptionPane.showMessageDialog(null, "Complete los campos");
    return;
    } 
    
    clsFacultades facultades = new clsFacultades();
    facultades.setFaculnombre(txtNombre.getText());
    facultades.setFaculstatus(txtEstado.getText());

    FacultadesDAO dao = new FacultadesDAO();
    clsBitacora bitacora = crearBitacora("Insertar facultades");
    dao.insertarFacultades(facultades, bitacora);

    JOptionPane.showMessageDialog(null, "Facultades registrado");
    
    

    llenadoDeTablas();
    limpiarTextos();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
            // TODO add your handling code here:       
        FacultadesDAO dao = new FacultadesDAO();
    if(txtBuscar.getText().isEmpty()){
    JOptionPane.showMessageDialog(null, "Ingrese un ID");
    return;
}
    int id = Integer.parseInt(txtBuscar.getText());

    clsBitacora bitacora = crearBitacora("Buscar facultades");
    clsFacultades facultades = dao.obtenerFacultadesPorId(id, bitacora);

    if(facultades != null){
        txtCodigo.setText(String.valueOf(facultades.getFaculcodigo()));
        txtNombre.setText(facultades.getFaculnombre());
        txtEstado.setText(facultades.getFaculstatus());
    }else{
        JOptionPane.showMessageDialog(null, "Facultades no encontrado");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
//        // TODO add your handling code here:
    if(txtCodigo.getText().isEmpty()){
    JOptionPane.showMessageDialog(null, "Seleccione un registro");
    return;
    }
    


    clsFacultades facultades = new clsFacultades();
    facultades.setFaculcodigo(Integer.parseInt(txtCodigo.getText()));
    facultades.setFaculnombre(txtNombre.getText());
    facultades.setFaculstatus(txtEstado.getText());

    FacultadesDAO dao = new FacultadesDAO();
    clsBitacora bitacora = crearBitacora("Modificar facultades");
dao.actualizarFacultades(facultades, bitacora);

    JOptionPane.showMessageDialog(null, "Facultades modificado");

    llenadoDeTablas();
    limpiarTextos();
    
    
    }//GEN-LAST:event_btnModificarActionPerformed

